class PersonEncapsulation {
    private String name;
    private int age;

    public PersonEncapsulation() {
        this.name = "Geeks";
        this.age = 10;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}